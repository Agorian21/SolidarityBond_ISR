7.2/ Lutte contre la fraude - Cas spécifique des cookies d’identifiant d’équipement
Dans le cadre de la protection des consommateurs et de la lutte contre la fraude, BDE collecte,
par le biais d'un outil de son partenaire THREATMETRIX, un identifiant équipement (appelé également
"Device fingerprinting") afin de procéder au contrôle des commandes des Acheteurs dans le cadre de
la lutte contre la fraude.
Cet identifiant est généré à partir d'informations techniques relatives à l'équipement (PC, smartphone,
tablette, etc.) que l’Acheteur utilise lors de la passation d'une commande sur notre site internet.
L’Acheteur est informé de la collecte de ces informations dès sa première connexion
sur www.BDE.com par l'intermédiaire du bandeau d'information relatif aux cookies.
En continuant sa navigation sur notre site ou en cliquant sur la croix présente sur le bandeau
d'information, l’Acheteur accepte la collecte de l'identifiant équipement.
L’Acheteur peut retirer son consentement à la collecte de cet identifiant à tout moment en cliquant
sur l'onglet "Gérer mes cookies" ci-dessous et en désactivant les cookies "Analytique et
Personnalisation".
