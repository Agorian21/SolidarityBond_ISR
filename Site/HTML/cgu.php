4/ Droit de rétractation
4.1/ Principe du droit de rétractation :
Conformément aux dispositions des articles L. 221-18 et suivants du Code de la Consommation,
l’Acheteur dispose d’un délai de rétractation de quatorze (14) jours calendaires courant à compter du
lendemain de la réception du dernier Produit de sa commande.
L’Acheteur peut retourner, sans avoir à justifier de motifs ni à payer de pénalités, à l'exception des
frais de retour qui restent à sa charge, tout article, dans un état permettant sa remise en vente (ceci
supposant qu’il soit retourné dans son emballage d’origine ou, à tout le moins, dans un emballage
permettant une protection équivalente dudit article au cours de son transport retour) et d’en
demander le remboursement.
Pour retourner un ou plusieurs Produits, l’Acheteur doit prendre contact avec le Vendeur en se rendant
sur le Service Client en ligne sur le Site. Les informations concernant sa commande et son paiement
sont accessibles dans son Espace Client, dans les rubriques « Mes commandes » et « Questions
fréquentes (FAQ) ».
En sélectionnant dans la FAQ « Je souhaite me rétracter », l’Acheteur accède à la messagerie interne
du Site par laquelle il peut transmettre sa demande de rétractation au Vendeur, lequel lui indiquera
par retour de courrier électronique, les modalités de renvoi du Produit.
Le retour d'un colis doit être soigneusement préparé selon les consignes données par le Vendeur,
notamment il doit être retourné avec tous les accessoires livrés (cordons, garanties, notices, logiciels,
etc.) dans son emballage d’origine ou, à tout le moins, dans un emballage permettant une protection
équivalente du Produit, et envoyé à l’adresse du service compétent qui a été communiquée à
l’Acheteur par le Vendeur. Pour les Produits tels que les smartphones, tablettes, ordinateurs les
données contenues dans ces Produits doivent être supprimées, et les comptes téléphoniques
désassociés.
L’Acheteur a quatorze (14) jours pour renvoyer le Produit au Vendeur.
Conformément aux dispositions légales, le Vendeur :
- Dispose de quatorze (14) jours pour rembourser l’Acheteur, à compter de la date à laquelle il
est informé de sa décision de rétractation.
- Peut différer le remboursement jusqu'à récupération des biens.
BDE procèdera au remboursement de l’Acheteur sur ordre du Vendeur et cela via le même
moyen de paiement que celui utilisé pour payer sa commande. Ainsi, si l’Acheteur a payé sa commande
à l’aide d’une carte bancaire (au comptant ou en 4X), BDE procédera au remboursement de
l’Acheteur par recrédit sur sa carte bancaire.
