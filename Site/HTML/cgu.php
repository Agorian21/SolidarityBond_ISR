base du consentement de l’Acheteur ou de son intérêt légitime).
En règle générale, les données personnelles de l’Acheteur sont conservées au sein de l’Union
Européenne. Cependant, étant donné que certains des Vendeurs ou de nos prestataires de service sont
situés dans des pays en dehors de l’Union Européenne (« Pays Tiers »), BDE et/ou le Vendeur
transfèrent certaines de ses données dans des Pays Tiers. Cela peut notamment être le cas vers des
Pays Tiers pour lesquels la Commission Européenne n’a pas évalué le niveau de conformité.
Dans un tel cas, BDE et/ou le Vendeur s’assurent que ce transfert est effectué en conformité
avec la réglementation applicable et garantissent un niveau de protection adéquat de la vie privée et
des droits fondamentaux de l’Acheteur (notamment par l’utilisation de clauses contractuelles types de
la Commission Européenne).
Sur simple demande formulée au Délégué à la protection des données, celui-ci peut fournir davantage
d’informations à l’Acheteur quant à ces transferts.
